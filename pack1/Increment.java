package pack1;

public class Increment {
    public static void main(String[] args) {
        // create var 
        int n = 10; 
        // incrmnt 
        n = n + 5; 
        System.out.println(n);
        n+=5; // incremente par 5 (plus courte)
        System.out.println(n);
        n+=1; 
        System.out.println(n);
        n++; // increment par 1
        System.out.println(n);
        n--; // decremente par 1
        System.out.println(n);
        --n; 
        System.out.println(n);

        // print
    }    
        
}
